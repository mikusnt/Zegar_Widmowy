static uint8_t TranslateToAlphabet(char cSign) {
	// male znaki
	if ((cSign >= 97) && (cSign <= 122)) return cSign - 61;
	// du¿e znaki
	else if ((cSign >= 65) && (cSign <= 90)) return cSign - 65;
	// cyfry
	if ((cSign >= 48) && (cSign <= 57)) return cSign - 22;
	// spacja
	else if (cSign == 32) return 67;
	// .
	else if (cSign == 46) return 63;
	// ,
	else if (cSign == 44) return 64;
	// !
	else if (cSign == 33) return 65;
	// ?
	else if (cSign == 63) return 66;
	// : z odstepem
	else if (cSign == 58) return 68;
	// : bez odstepu
	else if (cSign == 35) return 69;
	// smile
	else if (cSign == 0x7F) return 70;
	// -
	else if (cSign == 45) return 71;
	// symbol braku znaku
	else return 62;
}

static void LoadTextFromE(char buffer[], const char *data) {
	uint8_t i = 0;
	while (pgm_read_byte(data) != 0x00)
		buffer[i++] = pgm_read_byte(data++);
	buffer[i] = '\0';
}

uint8_t LoadSignByte(char cSign, uint8_t uiByteNr) {
	if (uiByteNr > 4) return 0;
	else return ALPHABET_YX(TranslateToAlphabet(cSign), uiByteNr);
} // END uint8_t LoadSignByte

uint8_t LoadSignLength(char cSign) {
	return AL_LENGTH(TranslateToAlphabet(cSign));
} // END uint8_t LoadSignByte

